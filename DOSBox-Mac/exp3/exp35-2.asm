DATA  SEGMENT
STR1  DB 30H,31H,32H,33H,34H,35H,36H,37H,38H,39H,40H,41H,42H,43H,44H,45H
COUNT EQU $-STR1
STR2  DB COUNT DUP(?)    
DATA  ENDS

CODE SEGMENT    
ASSUME DS:DATA,ES:DATA,CS:CODE
START: MOV AX,DATA
       MOV DS,AX    
       MOV ES,AX    ;初始化DS和ES
       LEA SI,STR1
       LEA DI,STR2  ;初始化偏移地址
       MOV CX,COUNT ;初始化串长度
       CLD          ;DF清零表示指针+
       REP MOVSB    ;重复移动
       MOV AH,4CH
       INT 21H
CODE ENDS
END START
